package day9;

public class TypeCasting {
    public static void main(String[] args) {
        int i = 10;
        double j = i;

        double x= 25.0;
        int y= (int)x;


        //widening conversion Type Casting
        System.out.println(j);
        System.out.println("narrowing type casting:    "+ y);

    }
}
